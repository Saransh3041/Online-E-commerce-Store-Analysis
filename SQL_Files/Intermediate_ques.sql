---Q1 Calculate the number of orders per month in 2018
SELECT 
    MONTHNAME(order_purchase_date) AS month_name,
    COUNT(DISTINCT (order_id)) AS total_orders
FROM
    orders_table
    where year(order_purchase_date)=2018 and order_status not in ('Canceled','Unavailable')
GROUP BY month_name , MONTH(order_purchase_date)
ORDER BY MONTH(order_purchase_date)

---Q2 Find the average number of products per order, grouped by customer city. 

SELECT 
    city, ROUND(AVG(order_qty), 1) AS per_order_qty
FROM
    (SELECT 
        c.customer_city AS city,
            oi.order_id,
            COUNT(oi.product_id) AS order_qty
    FROM
        order_item oi
    JOIN orders_table ot ON oi.order_id = ot.order_id
    JOIN customers c ON ot.customer_id = c.customer_id
    GROUP BY c.customer_city , oi.order_id) AS base1
GROUP BY city
ORDER BY per_order_qty DESC LIMIT 10


