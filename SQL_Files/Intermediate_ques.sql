---Q4 Identify the correlation between product price and the number of times a product has been purchased
SELECT 
        product_id as top_Selling_Product,
            AVG(price) AS avg_price,
            COUNT(product_id) AS purchased_qty
    FROM
        order_item
    GROUP BY product_id order by purchased_qty desc limit 10 

---Q5 Calculate the total revenue generated by each seller, and rank them by revenue
SELECT 
    seller as top10_seller, ROUND(SUM(revenue_by_seller), 2) AS total_rev
FROM
    (SELECT 
        oi.order_id,
            oi.seller_id AS seller,
            (oi.price / b.price_value) * ov.total_value AS revenue_by_seller
    FROM
        order_item oi
    JOIN order_value ov ON oi.order_id = ov.order_id
    JOIN (SELECT 
        order_id, ROUND(SUM(price), 2) AS price_value
    FROM
        order_item
    GROUP BY order_id) AS b ON oi.order_id = b.order_id) AS revenue_generated_by_sellers
GROUP BY seller
ORDER BY total_rev DESC limit 10







