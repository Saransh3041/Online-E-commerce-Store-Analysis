(select oi.order_id, oi.product_id,oi.price, ((oi.price/op.price_value)*ov.total_value) as product_revenue from order_item oi join order_value ov on oi.order_id=ov.order_id  join (SELECT order_id, round(sum(price),2) as price_value from order_item group by order_id) as op on oi.order_id=op.order_id) as a join products p on a.product_id=p.product_id group by p.product_category) as base order by rev_percent desc limit 10
